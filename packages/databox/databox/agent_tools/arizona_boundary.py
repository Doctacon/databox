_BOUNDARY_TOLERANCE = 1e-9
_MIN_LONGITUDE = min(point[0] for point in ARIZONA_BOUNDARY_LON_LAT)
_MAX_LONGITUDE = max(point[0] for point in ARIZONA_BOUNDARY_LON_LAT)
_MIN_LATITUDE = min(point[1] for point in ARIZONA_BOUNDARY_LON_LAT)
_MAX_LATITUDE = max(point[1] for point in ARIZONA_BOUNDARY_LON_LAT)


def is_in_arizona(latitude: float, longitude: float) -> bool:
    """Return whether a point is inside or on the generalized Census boundary."""

    if not (
        _MIN_LATITUDE <= latitude <= _MAX_LATITUDE and _MIN_LONGITUDE <= longitude <= _MAX_LONGITUDE
    ):
        return False

    inside = False
    for start, end in zip(
        ARIZONA_BOUNDARY_LON_LAT[:-1],
        ARIZONA_BOUNDARY_LON_LAT[1:],
        strict=True,
    ):
        if _point_on_segment(longitude, latitude, start, end):
            return True
        start_lon, start_lat = start
        end_lon, end_lat = end
        crosses_latitude = (start_lat > latitude) != (end_lat > latitude)
        if not crosses_latitude:
            continue
        crossing_longitude = start_lon + (
            (latitude - start_lat) * (end_lon - start_lon) / (end_lat - start_lat)
        )
        if longitude < crossing_longitude:
            inside = not inside
    return inside


def _point_on_segment(
    longitude: float,
    latitude: float,
    start: tuple[float, float],
    end: tuple[float, float],
) -> bool:
    start_lon, start_lat = start
    end_lon, end_lat = end
    cross = (longitude - start_lon) * (end_lat - start_lat) - (latitude - start_lat) * (
        end_lon - start_lon
    )
    if abs(cross) > _BOUNDARY_TOLERANCE:
        return False
    return (
        min(start_lon, end_lon) - _BOUNDARY_TOLERANCE
        <= longitude
        <= max(start_lon, end_lon) + _BOUNDARY_TOLERANCE
        and min(start_lat, end_lat) - _BOUNDARY_TOLERANCE
        <= latitude
        <= max(start_lat, end_lat) + _BOUNDARY_TOLERANCE
    )
